const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    id: String!
    born: Int!
    bookCount: Int
  }

  type Query {
    bookCount: Int!
    allBooks(author: String, genre: String): [Book!]

    authorCount: Int!
    allAuthors: [Author!]
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      id: Int
      genres: [String]!
    ): Book!
    editAuthor(name: String!, setBornTo: Int!): Author
    addAuthor(name: String!, id: String, born: Int!): Author
  }
`

const resolvers = {
  Query: {
    //Book resolvers
    bookCount: async () => Book.collection.countDocuments(),
    allBooks: async (root, args) => {
      return Book.find({}) //don´t query authors, it´s mongo object
    },

    //Author resolvers
    authorCount: async () => Author.collection.countDocuments(),
    allAuthors: async () => {
      return Author.find({})
    },
  },
  Mutation: {
    //Book mutations
    addBook: async (root, args) => {
      if (args.title.length < 4)
        throw new UserInputError('Book title too short')

      const author = await Author.find({ ...args.author })
      const authorId = author[0]._id.toString()
      const book = new Book({ ...args, author: authorId })

      try {
        await book.save()
      } catch (error) {
        throw new UserInputError(error.message, {
          invalidArgs: args,
        })
      }
      return book //return errors because author field is now mongo object instead of gql
    },
    //Author mutations
    addAuthor: async (root, args) => {
      if (args.name.length < 4)
        throw new UserInputError('Author name too short')

      const author = new Author({ ...args })
      try {
        await author.save()
      } catch (error) {
        throw new UserInputError(error.message, {
          invalidArgs: args,
        })
      }
      return author
    },
    editAuthor: async (root, args) => {
      const author = await Author.findOne({ name: args.name })
      author.born = args.setBornTo

      try {
        await author.save()
      } catch (error) {
        throw new UserInputError(error.message, {
          invalidArgs: args,
        })
      }
      return author
    },
  },
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
